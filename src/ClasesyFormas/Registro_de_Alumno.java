    public void insertuser() {
        if ((NombreTXT.getText().equals("")) || (AperllidoTXT.getText().equals("")) || (CodigoTXT.getText().equals("")) || (MateriaTXT.getText().equals(""))|| (NotaTXT.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Debe completar los campos para poder registrarse");

        } else {
          
                try {
                    ps = C.getConection().prepareStatement("INSERT INTO alumnos (Id_A,Nombre,Apellido,Materia,Calificacion) VALUES(?,?,?,?,?) ");
                    ps.setInt(1, Integer.parseInt(CodigoTXT.getText()));
                    ps.setString(2, this.NombreTXT.getText());
                    ps.setString(3, this.AperllidoTXT.getText());
                    ps.setString(4, this.MateriaTXT.getText());
                    ps.setInt(5, Integer.parseInt(this.NotaTXT.getText()));
                    int res = ps.executeUpdate();

                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Alumno Guardado");
                        limpiar();
                    } 
                    C.getConection().close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error al Guardar Alumno, Consulte con Administrador");
                    limpiar();
                    System.err.println(e);
                }
        }

    }

