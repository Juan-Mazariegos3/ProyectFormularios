    public void insertarDatos() {
        try {

            ps = C.getConection().prepareStatement("INSERT INTO alumnos (Id_A,Nombre,Apellido,Materia,Calificacion) VALUES(?,?,?,?,?) ");
            ps.setInt(1, Integer.parseInt(CodigoTXT.getText()));
            ps.setString(2, txtNOMBREALUM.getText());
            ps.setString(3, txtAPELLIDO.getText());
            ps.setString(4, txtCURSO.getText());
            ps.setString(5, txtCALIFICACION.getText());
            ps.execute();
            JOptionPane.showMessageDialog(null, "Registro Exitoso");
            limpiarDatos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Registrar");
            limpiarDatos();
        }
    }
