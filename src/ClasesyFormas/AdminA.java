            ps.setString(2, txtNOMBREALUM.getText());
            ps.setString(3, txtAPELLIDO.getText());
            ps.setString(4, txtCURSO.getText());
            ps.setString(5, txtCALIFICACION.getText());
            ps.setInt(6, Integer.parseInt(CodigoTXT.getText()));
