    private void cmdEDITAR1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEDITAR1ActionPerformed
        try {
            ps = C.getConection().prepareStatement("SELECT * FROM usurios WHERE ID_U = ?  ");
            ps.setInt(1, Integer.parseInt(UserTXT.getText()));
            rs = ps.executeQuery();
            if (rs.next()) {
                UserTXT.setText(rs.getString("ID_U"));
                ContraTXT.setText(rs.getString("Contra"));
                CodigoATXT.setText(rs.getString("Id_A"));

                mostrarDatosB();
            } else {
                JOptionPane.showMessageDialog(null, "No existe Codigo del Alumno");
                limpiarDatos();
                mostrarDatos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar Datos");
            limpiarDatos();
        }
    }//GEN-LAST:event_cmdEDITAR1ActionPerformed

